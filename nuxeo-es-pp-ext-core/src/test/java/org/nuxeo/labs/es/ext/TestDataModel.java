package org.nuxeo.labs.es.ext;

import org.junit.Assert;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.nuxeo.ecm.core.api.CoreSession;
import org.nuxeo.ecm.core.api.DocumentModel;
import org.nuxeo.ecm.core.test.CoreFeature;
import org.nuxeo.ecm.core.test.annotations.Granularity;
import org.nuxeo.ecm.core.test.annotations.RepositoryConfig;
import org.nuxeo.runtime.test.runner.Deploy;
import org.nuxeo.runtime.test.runner.Features;
import org.nuxeo.runtime.test.runner.FeaturesRunner;

import javax.inject.Inject;

@RunWith(FeaturesRunner.class)
@Features({ CoreFeature.class })
@RepositoryConfig(cleanup = Granularity.METHOD)
@Deploy({ "nuxeo-es-pp-ext-core" })
public class TestDataModel {

    @Inject
    CoreSession session;

    @Test
    public void TestRankingFacet() {
        DocumentModel doc = session.createDocumentModel(session.getRootDocument().getPathAsString(),"File","File");
        doc = session.createDocument(doc);
        Assert.assertTrue(doc.hasFacet("Ranking"));
        Assert.assertTrue(doc.hasSchema("ranking"));
    }

}
